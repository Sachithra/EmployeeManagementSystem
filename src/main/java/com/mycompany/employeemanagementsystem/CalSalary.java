    private void otbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_otbtnActionPerformed
      
        if(otamount.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Please Enter the O.T amount");
            
        }else{
      
       salaryAbstract calculate=new salary();
       
       int OT=Integer.parseInt(othr.getValue().toString());
       int sum=calculate.CalOt(OT);
       otamount.setText(String.valueOf(sum));
       
       
       int fullSalary=Integer.parseInt(salaryAmount.getText());
       int otAmount=Integer.parseInt(otamount.getText());
       
       int sumFull=calculate.FullSalary(fullSalary, OT);
       full.setText(String.valueOf(sumFull));
       
       
       if(full.getText().length()>0){
           full.setEnabled(false);
           full.setText( String.valueOf( calculate.FullSalary(fullSalary,
           otAmount)));
           
       }else{
           full.setEnabled(true);
       }
     
        }
    }//GEN-LAST:event_otbtnActionPerformed


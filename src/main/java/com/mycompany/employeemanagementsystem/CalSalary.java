    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      
       salaryAbstract calculate=new salary();
       
       int Days=Integer.parseInt(days.getText());
       int Sfp=Integer.parseInt(sfp.getText());
       int Hr=Integer.parseInt(hrs.getValue().toString());
       
       if(days.getText().isEmpty()){
           
       JOptionPane.showMessageDialog(null, "ENTER THE WORKING DAYS "
       + "OF EACH EMPLOYEE");
           
       }else if(sfp.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "ENTER THE PAYMENT PER DAY");
            
       }else{
          
          int sum=calculate.Salary(Days, Sfp, Hr);
          salaryAmount.setText(String.valueOf(sum));  
          
          if(salaryAmount.getText().length()>0){
              salaryAmount.setEnabled(false);
              
          }else{
              salaryAmount.setEnabled(true);
              
          }
           
       }
   
    }//GEN-LAST:event_jButton1ActionPerformed

